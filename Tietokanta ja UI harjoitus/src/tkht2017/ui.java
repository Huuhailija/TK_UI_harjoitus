    private void StartButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_StartButtonActionPerformed
        boolean jatketaan = backEnd.startCon();
         
        if (jatketaan) {
            CardLayout card = (CardLayout)mainPanel.getLayout();
            card.next(mainPanel);
            backEnd.initialize();
            
            if (backEnd.getError().isEmpty()) {
                JOptionPane.showMessageDialog(panel1,"Connection successful, good luck to the test!"); 
                textArea1.setText(backEnd.getExcerciseList().get(backEnd.currentExcercise()).getDescription());    
            }
            else {
                JOptionPane.showMessageDialog(panel1, backEnd.getError());
            }           
        } 
        else {
            JOptionPane.showMessageDialog(panel1, backEnd.getError());           
        }   
    }//GEN-LAST:event_StartButtonActionPerformed

