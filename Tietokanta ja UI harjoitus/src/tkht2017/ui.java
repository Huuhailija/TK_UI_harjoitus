    private void NextButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_NextButtonActionPerformed
        String answer = textArea2.getText();
            if (answer.equals(backEnd.getExcerciseList().get(backEnd.currentExcercise()).example_answer)) {
                backEnd.nextExcercise();
                backEnd.endTry(answer);
                backEnd.startTry();
                backEnd.resetTry();
                textArea2.setText(""); 
                textArea1.setText(backEnd.getExcerciseList().get(backEnd.currentExcercise()).getDescription());

            }
            else {
                if (backEnd.currentTry() != 3) {
                    backEnd.endTry(answer);
                    backEnd.nextTry();
                    backEnd.startTry();
                    JOptionPane.showMessageDialog(panel1, "Wrong answer try again.");
                    textArea2.setText("");
                   }
                    
                else {               
                    if(backEnd.getExcerciseList().size() == backEnd.currentExcercise() + 1  && backEnd.currentTry() == 3 ) {
                        backEnd.endTry(answer);
                        backEnd.resetTry();
                        JOptionPane.showMessageDialog(panel1, "Test over closing program.");
                        dispose();
                    }
                    else {
                        backEnd.endTry(answer);
                        JOptionPane.showMessageDialog(panel1, "3rd wrong answer, moving to next question.");
                        backEnd.nextExcercise();
                        backEnd.resetTry();
                        backEnd.startTry();
                        textArea2.setText("");
                        textArea1.setText(backEnd.getExcerciseList().get(backEnd.currentExcercise()).getDescription()); 
                    }
                    }      
            } 
    }//GEN-LAST:event_NextButtonActionPerformed
       
